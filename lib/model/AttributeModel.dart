class AttributeModel {
  late int primaryIndex;
  late int secondaryIndex;

  int get getPrimaryIndex {
    return primaryIndex;
  }

  set setPrimaryIndex(int index) {
    primaryIndex = index;
  }

  int get getSecondaryIndex {
    return secondaryIndex;
  }

  set setSecondaryIndex(int index) {
    secondaryIndex = index;
  }


}